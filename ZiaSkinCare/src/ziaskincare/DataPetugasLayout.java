    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
       
        DataPetugas dataPetugas = new DataPetugas();
        dataPetugas.setNama(txtNamaPetugas.getText());
        dataPetugas.setUser(txtUserNama.getText());
        int x = cmbLevel.getSelectedIndex();
        String level="";
         if (x==1)
                level="Admin";
            else if (x==2)
                level="User";          
        dataPetugas.setLevel(x); 
        dataPetugas.setNoTelp(txtNoTelpon.getText());
        dataPetugas.setAlamat(txtAlamatPetugas.getText());
        dataPetugas.setPassword(txtPassword.getText());
        dataPetugasDao.save(dataPetugas);
        JOptionPane.showMessageDialog(null, MessageUtil.successSaveMessage());
        getListDataPetugas();
        clear();
    }//GEN-LAST:event_btnSaveActionPerformed

