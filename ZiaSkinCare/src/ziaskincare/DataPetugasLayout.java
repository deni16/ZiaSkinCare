    private void tblPetugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPetugasMouseClicked
        // TODO add your handling code here:
        int baris = tblPetugas.getSelectedRow();
        String namaTerpilih = tblPetugas.getValueAt(baris, 0).toString();
        try {
            selecteddataPetugas = dataPetugasDao.getEntityByNama(namaTerpilih);
         //   PreparedStatement pStatement = null;
            txtNamaPetugas.setText(selecteddataPetugas.getNama());
            txtUserNama.setText(selecteddataPetugas.getUser());
            selecteddataPetugas.getLevel();
            txtNoTelpon.setText(selecteddataPetugas.getNoTelp());
            txtAlamatPetugas.setText(selecteddataPetugas.getAlamat());
            btnDelete.setEnabled(true);
            btnSave.setEnabled(false);  
        } catch (SQLException ex) {
            Logger.getLogger(DataPetugasLayout.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblPetugasMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try{
        dataPetugasDao.delete(selecteddataPetugas);
        JOptionPane.showMessageDialog(null, MessageUtil.successDeleteMessage());
        getListDataPetugas();
        clear();
         } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, MessageUtil.failureDeleteMessage());
            Logger.getLogger(DataSatuanLayout.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

