public void getListDataPetugas (){
    // set header table
    String [] judul ={"Nama","User","Level","No Telpon","Alamat"};
    DefaultTableModel data = new DefaultTableModel(null, judul);
    tblPetugas.setModel(data);
    List<DataPetugas> listDataPetugas = dataPetugasDao.getAllData();
    for (DataPetugas dataPetugas : listDataPetugas){
        data.addRow(new Object[]{dataPetugas.getNama(),dataPetugas.getUser(),dataPetugas.getLevel(),dataPetugas.getNoTelp(),dataPetugas.getAlamat()});
    }
}

