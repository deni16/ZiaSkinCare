    public void getListDataPelanggan (){
    // set header table
        String [] judul ={"No Member","Nama","Tanggal Member","No Telpon","Alamat"};
        DefaultTableModel data = new DefaultTableModel(null, judul);
        tblPelanggan.setModel(data);
        List<DataPelanggan> listDataPelanggan = dataPelangganDao.getAllData();
        for (DataPelanggan dataPelanggan : listDataPelanggan){
            data.addRow(new Object[]{dataPelanggan.getNoMember(),dataPelanggan.getNamaPelanggan(),dataPelanggan.getTanggalMember(),dataPelanggan.getNoTelp(),dataPelanggan.getAlamat()});
        }
    }


