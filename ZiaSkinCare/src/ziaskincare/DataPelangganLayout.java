    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        DataPelanggan dataPelanggan = new DataPelanggan();
        dataPelanggan.setId(Long.valueOf(RandomNumberUtil.getRandomNumber(9)));
        dataPelanggan.setNoMember(Integer.valueOf(txtNoMember.getText()));
        dataPelanggan.setNamaPelanggan(txtNamaMember.getText());
        dataPelanggan.setTanggalMember(txtTanggalJoinMember.getDate());
        dataPelanggan.setNoTelp(txtNoTelp.getText());
        dataPelanggan.setAlamat(txtAlamat.getText());
        dataPelangganDao.save(dataPelanggan);
        JOptionPane.showMessageDialog(null, MessageUtil.successSaveMessage());
        getListDataPelanggan();
        clear();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblPelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPelangganMouseClicked
        // TODO add your handling code here:
        int baris = tblPelanggan.getSelectedRow();
        int noMember = Integer.valueOf(tblPelanggan.getValueAt(baris, 0).toString()); 
        try {
            selectedDataPelanggan = dataPelangganDao.getEntityByNoMember(noMember);
            txtNoMember.setText(String.valueOf(selectedDataPelanggan.getNoMember()));
            txtNamaMember.setText(selectedDataPelanggan.getNamaPelanggan());
            txtTanggalJoinMember.setDate(selectedDataPelanggan.getTanggalMember());
            txtAlamat.setText(selectedDataPelanggan.getAlamat());
            txtNoTelp.setText(selectedDataPelanggan.getNoTelp());
            btnEdit.setEnabled(true);
            btnSave.setEnabled(false);
            btnDelete.setEnabled(true);
            } catch (SQLException ex) {
            Logger.getLogger(DataPelangganLayout.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblPelangganMouseClicked

